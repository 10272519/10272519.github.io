//Form button function to show popup 
function msg(){	
	var x=document.forms["message"]["comments"].value;
	var y=document.forms["message"]["name"].value;
	var z=document.forms["message"]["email"].value;
		if (x=="")
		{alert("Please enter your message");}
	
		else if (y=="")
		{alert("Please enter your name");}
	
		else if (z=="")
		{alert("Please enter a valid email address");}
	
		else
		{alert("Your message has been sent");}
}
	
